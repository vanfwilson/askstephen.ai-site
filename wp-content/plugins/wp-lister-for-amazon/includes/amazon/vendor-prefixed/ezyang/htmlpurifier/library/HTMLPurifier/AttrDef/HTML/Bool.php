class WPLab_Amazon_HTMLPurifier_AttrDef_HTML_Bool extends WPLab_Amazon_HTMLPurifier_AttrDef
{

    /**
     * @type string
     */
    protected $name;

    /**
     * @type bool
     */
    public $minimized = true;

    /**
     * @param bool|string $name
     */
    public function __construct($name = false)
    {
        $this->name = $name;
    }

    /**
     * @param string $string
     * @param WPLab_Amazon_HTMLPurifier_Config $config
     * @param WPLab_Amazon_HTMLPurifier_Context $context
     * @return bool|string
     */
    public function validate($string, $config, $context)
    {
        return $this->name;
    }

    /**
     * @param string $string Name of attribute
     * @return WPLab_Amazon_HTMLPurifier_AttrDef_HTML_Bool
     */
    public function make($string)
    {
        return new WPLab_Amazon_HTMLPurifier_AttrDef_HTML_Bool($string);
    }
}

